        <h5>Admin Panel - The other side of the website</h5>

        <p>Admin panel is like a special CMS where admins of website can do stuff, regular users can't like adding new products or managing users and manage stock .. etc.
        The first page of admin panel is actually a dashboard where statistics of products and data about best seller products will be organized in a proper layout that allows 
        admins to see everything.
        Admin of course needs to add categories and suppliers of products, and payment methods and products .. etc. Every entity needs some kind of management so we can add, edit and delete entities, and all of 
