        <h3>General points of the project</h3>
        <p>Like any other e-commerce website, the user redirected to the website or access it through a web engine (e.g. google, bing).
        The website has a title (temporary), a header that contains the logo, search section, and some navigation buttons. underneath that, 
        there's the featured product (that will be choosen by an admin in the admin panel), and some informations about the service, and of course our best seller, and a section for getting
        letters from us, and footer [Very basic layout with simple things]</p>
        <p>When the user search for a product, we redirect the user to a result page where he'll find the results. But all of that cannot be done if we don't have product stored 
